import React, { Component } from 'react';
import Cell from './Cell.js';

export default class Matrix extends Component {

  genRow = (vals) => (
    vals.map((val,i) => <Cell key={`C${val}-${i}`} value={val}/>)
  )

  genMatrix = () => (
    this.props.values.map((rowVals,i) => <div key={`R${i}`} className="row">{this.genRow(rowVals)}</div>)
  )

  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }

}

